function setLocal(name, value) {
    window.localStorage.setItem(name, value)
}

function getLocal(name) {
    return window.localStorage.getItem(name) || '';
}

function changeLg() {
    var lang = "en";
    if (getLocal('language') && getLocal('language') == 'zh') {
        lang = getLocal('language')
    } else if (getLocal('language') && getLocal('language') == 'hk') {
        lang = getLocal('language')
    }else if (getLocal('language') && getLocal('language') == 'kor') {
        lang = getLocal('language')
    }else if (getLocal('language') && getLocal('language') == 'jp') {
        lang = getLocal('language')
    }else if (getLocal('language') && getLocal('language') == 'th') {
        lang = getLocal('language')
    } else {
        setLocal('language', 'en');
    }
    var urls = JSON.stringify(window.location);
    if (urls.indexOf('black') != -1) {
        $("[data-localize]").localize("text", {
            pathPrefix: "../lang",
            language: lang
        });
    } else {
        $("[data-localize]").localize("text", {
            pathPrefix: "lang",
            language: lang
        });
    }
    // $("[data-localize]").localize("text", {
    //     pathPrefix: "lang",
    //     language: lang
    // });
    setLang(lang)

}
